
// FUNÇÕES MATH()

Math.floor() // arredonda para baixo um numero

Math.ceil() // arredonda para cima um numero

Math.round() // retorna um numero arredondado para o mais próximo

Math.max() //verifica qual numero de uma sequência é o maior

Math.min() //verifica o menor numero

Math.random() // gera um numero aleatório

Math.random() * (10 - 5) + 5 // numero aleatório entre 10 e 5

Math.PI() //entrega o valor de pi

Math.pow(2, 10) // eleva um numero sobre o outro



